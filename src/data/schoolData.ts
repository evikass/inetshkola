// ==================== ТИПЫ ДАННЫХ ====================
// Урок - самый низкий уровень
export interface Lesson {
  id: string
  title: string
  description: string
  theory: string
  examples: string[]
  completed: boolean
  difficulty: 'easy' | 'medium' | 'hard'
  estimatedTime: number // в минутах
}

// Тема - содержит уроки
export interface Topic {
  id: string
  title: string
  description: string
  lessons: Lesson[]
}

// Раздел - содержит темы
export interface Section {
  id: string
  title: string
  description: string
  topics: Topic[]
}

export interface QuizQuestion {
  id: string
  question: string
  options: string[]
  correctAnswer: number
  explanation: string
  difficulty: 'easy' | 'medium' | 'hard'
  points: number
}

export interface Subject {
  id: string
  title: string
  icon: string // Имя иконки
  color: string
  gradient: string
  description: string
  sections: Section[]
  quiz: QuizQuestion[]
}

export interface Grade {
  id: number
  name: string
  shortName: string
  subjects: Subject[]
}

